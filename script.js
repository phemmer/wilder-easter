function parse_points(points) {
    var lines = points.split("\n");
    var subworlds = [];
    var waypoints = [];
    for (var i=0; i<lines.length; i++) {
        var line = lines[i];
        var linetype = line.split(":", 2)[0]
        switch (linetype) {
            case "subworlds":
                subworlds = line.split(":", 2)[1].split(",");
                break;

            case "name":
                var fields = line.split(",");
                var kvfields = {};
                var firstfield;
                for (var j = 0; j < fields.length; j++) {
                    [k, v] = fields[j].split(":", 2);
                    if (j == 0) {
                        firstfield = k;
                    }
                    kvfields[k] = v;
                }
                waypoints.push(kvfields);
                break;
        }
    }

    return {subworlds:subworlds, waypoints: waypoints}
}
